package sdet.JSONPARSE;

public class JsonPojo {
    private String title;
    private String author;

    public String getTitle() {
        return title;
    }

    public JsonPojo setTitle(String title) {
        this.title = title;
        return this;
    }

    public String getAuthor() {
        return author;
    }

    public JsonPojo setAuthor(String author) {
        this.author = author;
        return this;
    }
}
